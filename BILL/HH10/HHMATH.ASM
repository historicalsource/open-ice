**************************************************************************
*								         *
* move_to_speed_x input	;a1 = old speed					 *
* 				;a2 = desired end speed			 *
* 				;a3 = amount to move close (0-255)	 *
* 		   output	;a1 = new speed				 *
* 	DO NOT TRASH A14						 *
*								         *
**************************************************************************
move_to_speed_x		;a3 = 0-255 = % of new vs old
	mmtm	sp,a2,a3,a4,a5,a6,a7
	sll	24,a3
	clr	a4
	clr	a7
	move	a1,a6
	movk	8,a5
mta	sra	1,a1
	sra	1,a2
	sll	1,a3
	jrnc	mtan
	add	a1,a4
	add	a2,a7
mtan	dsjs	a5,mta
	move	a6,a1
	sub	a4,a1
	add	a7,a1
	mmfm	sp,a2,a3,a4,a5,a6,a7
	rets

**************************************************************************
*								         *
* find_closest_distance							 *
* 	finds the shortest distance between two moving object		 *
*     	assumes objects are moving at a constant velocity		 *
* 	input	a8 = object 1						 *
* 		a9 = object 2						 *
* 	output	a0 = distance						 *
* find_closest_dist_r							 *
* 	finds the shortest distance between two moving object		 *
*     	assumes objects are moving at a constant velocity		 *
* 	input	a0 = x position difference				 *
* 		a1 = z position difference				 *
* 		a2 = xvel difference					 *
* 		a3 = zvel difference					 *
*								         *
**************************************************************************
find_closest_distance
	mmtm	sp,a1,a2,a3,a4,a5,a6,a7,a10,a11
	move	*a8(OXVAL),a14,L
	move	*a9(OXVAL),a0,L
	sub	a14,a0			;a0 = x0 difference
	move	*a8(OZVAL),a14,L
	move	*a9(OZVAL),a1,L
	sub	a14,a1			;a1 = z0 difference
	move	*a8(OXVEL),a14,L
	move	*a9(OXVEL),a2,L
	sub	a14,a2			;a2 = xvel difference
	move	*a8(OZVEL),a14,L
	move	*a9(OZVEL),a3,L
	sub	a14,a3			;a3 = zvel difference
	jruc	fcd_1
find_closest_dist_r
	mmtm	sp,a1,a2,a3,a4,a5,a6,a7,a10,a11
fcd_1	move	a2,a2
	jrz	fcd_x0			;xv difference = 0 ; special case
	move	a3,a3
	jrz	fcd_z0			;zv difference = 0 ; special case
	move	a0,a10
	mpys	a3,a10
	divs	a2,a10
	move	a1,a5
	sub	a10,a5			;a5 = a1 - a3 * a0 / a2 = z @x=0
	abs	a5
	move	a1,a10
	mpys	a2,a10
	divs	a3,a10
	move	a0,a4
	sub	a10,a4			;a4 = a0 - a2 * a1 / a3 = z @x=0
	abs	a4
	cmp	a4,a5
	jrhs	fcd2
	SWAP	a4,a5
	SWAP	a0,a2
	SWAP	a1,a3
fcd2	move	a4,a6
	move	a4,a7
	sra	16,a6
	sll	16,a7
	divs	a5,a6
	move	a6,a0
	callr	GETATAN			;a0 = atan (a4 / a5)
	callr	GETCOS
	mpys	a4,a0			;a0 = Xz0 * cos (a0) = distance
	mmfm	sp,a1,a2,a3,a4,a5,a6,a7,a10,a11
	rets
fcd_z0	move	a1,a0
fcd_x0	abs	a0
	srl	16,a0
	mmfm	sp,a1,a2,a3,a4,a5,a6,a7,a10,a11
	rets

