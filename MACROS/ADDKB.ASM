*ADDKB - ADD AN IMMEDIATE KONSTANT TO BYTE SIZED MEMORY.
*	ADDKB	IMMEDIATE,MEMORY
*RETURNS:
*	N = 1 IF THE RESULT IS NEGATIVE
*	C = 1 IF THERE IS A CARRY
*	Z = 1 IF THE RESULT IS 0
*	V = 1 IF THERE IS AN OVERFLOW
ADDKB	.macro	IMM,MEM
	MOVB	:MEM:,A14
	ADDK	:IMM:,A14
	MOVB	A14,:MEM:
	.endm

