**************************************************************************
*								         *
* START_GOALIE_ANIM - START GOALIE ANIMATION				 *
* 									 *
* PASS:									 *
* A6  = PLAYER BLOCK							 *
* A8  = OBJECT								 *
* A14 = ANIMATION TABLE							 *
* 									 *
* RETURN:								 *
* NUTIN'								 *
* 									 *
* TRASHES A0, A1, AND A14						 *
*								         *
**************************************************************************

START_GOALIE_ANIM0
	CALLA	start_panim_if_not
	JRZ	SGA_ABORT			;BR=SCRIPT SET
	JRUC	SGA_FLIP
START_GOALIE_ANIM	
	CALLA	start_panim_if_not_ta14
	JRZ	SGA_ABORT			;BR=SCRIPT SET
SGA_FLIP
	MOVE	*A8(OCTRL),A0,W
	ANDNI	M_FLIPH,A0
	MOVB	*A6(POF_DIRECTION),A1
	MOVB	*A6(POF_NUMBER),A14
	SUBK	4,A14
	JRNZ	SGA_RIGHT_GOALIE		;BR=RIGHT GOALIE

	SUBK	5,A1				;LEFT GOALIE CHECK
	JRP	SGA_SET				;BR=FLIPPED
	ORI	M_FLIPH,A0
	JRUC	SGA_SET
SGA_RIGHT_GOALIE
	DEC	A1				;DIRECTION 1 RIGHT GOALIE CHECK
	JRZ	SGA_SET				;BR=FLIPPED		
	SUBK	4-1,A1
	JRP	SGA_SET				;BR=FLIPPED
	ORI	M_FLIPH,A0
SGA_SET
	MOVE	A0,*A8(OCTRL),W
SGA_ABORT
	RETS

