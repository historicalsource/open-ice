**************************************************************************
*								         *
* SUBSTITUTION_PAGE - CHANGE TEAM LINE UP				 *
* 									 *
* PASS:									 *
* NUTIN'								 *
* 									 *
* RETURN:								 *
* NUTIN'								 *
