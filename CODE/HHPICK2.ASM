PENACHO_TO_ASCII_COUNT
	CMPI	26,A14
	JRLT	PTA_ALPHA			;BR=DA ALPHABET
	JREQ	PTA_SPACE			;BR=EMPTY SPACE
	MOVI	' ',A14				;Q-MARK
	RETS
PTA_SPACE
	MOVI	' ',A14
	INC	A11
	RETS
PTA_ALPHA
	ADDI	'A',A14
	RETS

