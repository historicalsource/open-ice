**************************************************************************
*								         *
* PUT_INITIALS_IN_INFOBOX						 *
* 									 *
* PASS:									 *
* A9 = PLAYER NUMBER - 1						 *
* RETURN:								 *
* NUTIN'								 *
*								         *
