* TOTAL_STAT - TOTAL A GIVEN PLAYER STATISTIC				 *
* 									 *
* PASS:									 *
* A0 = STATISTIC OFFSET INTO PLAYER BLOCK				 *
* A6 = PLAYER BLOCK ADDRESS						 *
* RETURN:								 *
* A1 = TOTAL								 *
*								         *
**************************************************************************

