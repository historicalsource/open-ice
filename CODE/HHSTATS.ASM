**************************************************************************
*								         *
* FIND_STAT_LEADERS							 *
*								         *
* PASS:									 *
* A0 = STAT OFFSET							 *
* RETURN:								 *
* A1 = LEADERS								 *
*								         *
**************************************************************************

FIND_STAT_LEADERS
	PUSH	A2
	MOVI	P1_STATS,A2
	ADD	A0,A2
	MOVB	*A2,A3				;DEFAULT HIGH

	MOVK	3,A4
FSLS_FIND_HIGH_LUPE
	ADDI	8*8,A2
	MOVB	*A2,A14
	CMP	A3,A14
	JRLE	FSLS_FHL_NEXT			;BR=NOT GREATER
	MOVE	A14,A3				;NEW HIGH
FSLS_FHL_NEXT
	DSJS	A4,FSLS_FIND_HIGH_LUPE

	CLR	A1
	MOVE	A3,A3
	JRZ	FSLS_DONE			;BR=NO LEADERS

	MOVI	P1_STATS,A2
	ADD	A0,A2
	MOVK	1,A5
	MOVK	4,A4
FSLS_FIND_LEADERS_LUPE
	MOVB	*A2,A14
	CMP	A3,A14
	JRLT	FSLS_FLL_NEXT			;BR=NOT HIGH
	OR	A5,A1				;SET LEADER
FSLS_FLL_NEXT
	ADDI	8*8,A2
	SLL	1,A5
	DSJS	A4,FSLS_FIND_LEADERS_LUPE
FSLS_DONE
	PULLQ	A2
	RETS

