	ADD	A14,A1
	ADDK	8,A6
	INC	A2
	CMP	A3,A2
	JRNE	TS_LUPE				;BR=STILL MORE PERIODS TO TOTAL
	MMFM	SP,A2,A3,A6
	RETS

