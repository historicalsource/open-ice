*								         *
**************************************************************************
A_WAIT_FACEOFF
	MOVE	@PUCK_CONTROL,A14,W
	JRNZ	A_GSCRIPT_DONE			;BR=UNDER CONTROL

	MOVE	@PUCK_MODE,A14,W
