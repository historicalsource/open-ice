	RETS

**************************************************************************
*								         *
* A_CHECK_KO_SLIDE								 *
* 									 *
* PASS:									 *
* A8 = PLAYER OBJECT							 *
*								         *
**************************************************************************

A_CHECK_KO_SLIDE
	MOVE	@PUCK_MODE,A4,W
	CMPI	PUM_FACEOFF,A4
	JREQ	ACKOS_STOP			;BR=ABORT, FACEOFF!

	MOVE	*A8(OXVEL),A0,L
	GETST	A3

	MOVB	*A8(ANIMSLP),A1
	DEC	A1
	JRZ	ACKOS_STOP			;BR=NO MORE LOOPS
	MOVB	A1,*A8(ANIMSLP)

	MOVI	07FFFH,A1
	MOVE	*A8(OXVAL),A2,L
	JRN	ACKOS_CHECK_LIMIT		;BR=LEFT SIDE
	NEG	A1
ACKOS_CHECK_LIMIT
	ABS	A2
	CMPI	029E0000H,A2
	JRLT	ACKOS_SLOW
	MOVE	A0,A1
	NEG	A1
ACKOS_SLOW
	ADD	A1,A0
	GETST	A14
	XOR	A3,A14
	ANDI	0A0000000H,A14
	JRZ	ACKOS_SET			;BR=GOING OTHER DIRECTION

	MOVE	A0,A0
	JRZ	ACKOS_SET

	CMPI	PUM_SCORE,A4
	JRNE	ACKOS_CLR			;BR=NO SCORE

	MOVI	PID_GOAL_SLIDE,A0
	CALLA	EXISTP_ALL
	JRNZ	ACKOS_CLR			;BR=GOAL ALREADY SLIDIN'

	CREATE	PID_GOAL_SLIDE,GOAL_SLIDE_PROC

ACKOS_CLR
	CLR	A0
ACKOS_SET
	MOVE	A0,*A8(OXVEL),L

	MOVE	*A8(ANIMFRM),A14,L		;HOLD FRAME
	SUBI	020H+010H+020H,A14
	MOVE	A14,*A8(ANIMFRM),L
	RETS

ACKOS_STOP
	CLR	A14
	MOVE	A14,*A8(OXVEL),L
	RETS

**************************************************************************
*								         *
* GOAL_SLIDE_PROC							 *
* 									 *
* PASS:									 *
* A8 = GOALIE OBJECT							 *
*								         *
**************************************************************************

GOAL_SLIDE_PROC
	MOVE	*A8(ODT_PBK),A6,L
	MOVB	*A6(POF_NUMBER),A14
	SUBK	4,A14
	JRNZ	GSP_RIGHT

	MOVE	@lgoalF_object,A8,L
	MOVI	-020000H,A9
	MOVI	01000H,A10
	MOVI	-02AD0000H,A11
	JRUC	GSP_LUPE

GSP_RIGHT
	MOVE	@rgoalF_object,A8,L
	MOVI	020000H,A9
	MOVI	-01000H,A10
	MOVI	02AD0000H,A11

GSP_LUPE
	ADD	A10,A9
	MOVE	A9,*A8(OXVEL),L
	JRZ	GSP_WAIT			;BR=STOPPED

	SLEEP	1
	JRUC	GSP_LUPE

GSP_WAIT
	SLEEP	1
	MOVE	@WORLDX+010H,A0,W
	ABS	A0
	CMPI	0163H,A0
	JRGT	GSP_WAIT

	MOVE	A11,*A8(OXVAL),L

	DIE


